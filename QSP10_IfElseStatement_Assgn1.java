class QSP10_IfElseStatement_Assgn1 {
    public static void main(String[] args) {

        String tL= "green";
        if (tL == "green")
        {
            System.out.println("You can go");
        }
        else if (tL == "red")
        {
            System.out.println("Please stop");
        }
        else if (tL == "orange")
        {
            System.out.println("Just wait, see properly then go");
        }
        else
        {
            System.out.println("Wrong Input...");
        }

    }
}
