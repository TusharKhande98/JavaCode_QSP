class QSP8_IncDecOptr {
    public static void main(String[] args) {


        // 1. int increment & decrement
        int a = 100;
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println("\n");

        // 2. long increment & decrement
        long b = 454546l;
        System.out.println(++b);
        System.out.println(b++);
        System.out.println(++b);
        System.out.println(--b);
        System.out.println(b--);
        System.out.println(b);
        System.out.println("\n");

        // 3. float increment & decrement
        float c = 3.14f;
        System.out.println(++c);
        System.out.println(c++);
        System.out.println(++c);
        System.out.println(--c);
        System.out.println(c--);
        System.out.println(c);
        System.out.println("\n");

        // 4. double increment & decrement
        double d = 31.4545454d;
        System.out.println(++d);
        System.out.println(d++);
        System.out.println(++d);
        System.out.println(--d);
        System.out.println(d--);
        System.out.println(d);
        System.out.println("\n");

        // 5. char increment & decrement
        char e = 'T';
        System.out.println(++e);
        System.out.println(e++);
        System.out.println(++e);
        System.out.println(--e);
        System.out.println(e--);
        System.out.println(e);



    }
}
