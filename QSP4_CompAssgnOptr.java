        int k = 666666;
        k*=3;
        long l = 8526515555L;
        l*=2;
        float m = 3.27f;
        m*=3;
        double n = 78.23570;
        n*=2;

        System.out.println(h);
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
        System.out.println(l);
        System.out.println(m);
        System.out.println(n);
        System.out.println(" ");

    // 3. with Division

        byte o = 60;
        o /=12;
        short p = 785;
