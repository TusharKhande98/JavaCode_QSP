class QSP5_RelationalOptr {
    public static void main(String[] args) {

        System.out.println(10.12f == 10.12d); // false
        System.out.println(123456789 == 123456789l); // true
        System.out.println(97<'b');  // true
        System.out.println(1111<1111.00);  // false
        System.out.println('B' == 66);  // true
        System.out.println(true == true);  // true
        //    System.out.println(true>false);  // CTE
        System.out.println(30.0f == 30);  // true
        System.out.println('A'> 'a');  // false





    }
}
